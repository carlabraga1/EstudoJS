// como declarar

var variavel = 'carlin'
variavel = 'braga'
console.log(variavel);

// typeof = o tipo da variavel 

// var = escopo global e local , pode ter seu valor alterado , se não tiver um valor ele será null 

let variavel2 = 'carlin'
variavel2 = 'braga'
console.log(variavel2) ;

// let = escopo local de bloco , pode ter seu valor alterado , se não tiver um valor também sera declarado como null.


const variavel3 = 'carlin';
console.log(variavel3);

// const = escopo local de bloco , não pode ser alterado , somente leitura e precisar tem um valor .

//-----------------------------------------//

// ESCOPO
// escopo define uma limitação e visibilidade de um bloco de código.

// Escopo global = quando a variavel é declarada fora de qualquer bloco , sua visibilidade fica disponivel em todo o codigo.

// Escopo Local = quando a variavel é declarada dentro de um bloco, sua visibilidade pode ficar disponivel ou não.

//-------------------------------------//

