// Operadores Relacionais 

/* TIPOS DE OPERADORES QUE CONSULTAM A RELAÇÃO ENTRE VALORES: 
> maior que ; 
< menor que ;
>= maior ou igual a;
<= menor ou igual a;
*/

// EXEMPLOS :

var maiorQue = 5 > 2 ;
console.log(maiorQue);

var menorQue = 5 < 2;
console.log(menorQue);

var maiorIgual = 5 >= 2;
console.log(maiorIgual);

var menorIgual = 5 <= 2; 
console.log(menorIgual); 