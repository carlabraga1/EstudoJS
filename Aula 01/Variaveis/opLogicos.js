// OPERADORES LOGICOS 

// São tipos de operaods que consultam os valores logicos:

/* && = "e" - considera que todos os valores sejam true ;
|| = "ou" - considera que qualquer valor seja true;
!- = "não" - inverte o valor de true para false ou vice-versa */


// EXEMPLOS :
 
var e = true && true;
console.log(e);

var ou = false || false;
console.log(ou);

var nao = !true;
console.log(nao);